def swap(x,y):
    temp = x
    x = y
    y = temp
    print("after swapping number will 1st number :", x)
    print("after swapping number will 2nd number :", y)
print("enter 2 values to swap:")
a=int(input())
b=int(input())
print("before swapping number will 1st number :",a)
print("before swapping number will 2nd number :",b)
swap(a,b)










